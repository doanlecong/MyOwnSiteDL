    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $this->middleware('auth');
        $file = MyServiceFile::find($id);
        if($file) {
            $fileType = $file->file_type;
            $contentType = ConfigData::getContentTypeResponseBaseFileType($fileType);
            $fileContent = Storage::disk('local')->get($file->file_name);
            return (new Response($fileContent,200))->header('Content-Type', $contentType);
        }
        return \response('The resource is not available!', 404);
    }

